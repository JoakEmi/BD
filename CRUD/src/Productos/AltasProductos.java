    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if(!txtNombreProducto.getText().equals("") && !txtMarca.getText().equals("") && !txtCategoria.getText().equals("")
              && !txtStock.getText().equals("") && !txtPrecioVenta.getText().equals("") && !txtPrecioCompra.getText().equals("") ){
            try{

                 Connection con1;
                 Conexion conect1 = new Conexion();
                 con1 = conect1.getConnection();
                 String sql = "INSERT INTO Productos (NombreProducto, IdMarca," +
                              "IdCategoria, Stock, PrecioVenta, PrecioCompra)"
                            + "VALUES('" + txtNombreProducto.getText() + "','" + txtMarca.getText() + "','" + 
                                           txtCategoria.getText() + "','" + txtStock.getText() + "','" + 
                                           txtPrecioVenta.getText() + "','" + txtPrecioCompra.getText() +"')";

                try (Statement st = con1.createStatement()) {
                    st.executeUpdate(sql);
                }
                conect1.Desconexion();
                 
             }catch(SQLException e){
                 JOptionPane.showMessageDialog(null, e,
                        "Error", JOptionPane.OK_OPTION);
             
             }
        }else
            JOptionPane.showMessageDialog(null, "Inserta todos los datos",
                        "Error", JOptionPane.OK_OPTION);
            
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

